def sum(a, b):
    """
    Ajoute deux nombres ensemble.

    a : premier nombre
    b : deuxième nombre

    Retourne : la somme de a et b
    """
    return a + b


def subtraction(a, b):
    """
    Soustrait deux nombres.

    a : premier nombre
    b : deuxième nombre

    Retourne : le résultat de a - b
    """
    return a - b
